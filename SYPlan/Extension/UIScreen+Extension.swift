import UIKit

extension UIScreen {
    
    /// Current screen width
    class var width: CGFloat {
        return UIScreen.main.bounds.width
    }
    
    /// Current scteen height
    class var height: CGFloat {
        return UIScreen.main.bounds.height
    }
    
}
